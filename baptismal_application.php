if (!function_exists('notifyIndividualFromAdmin_mysqli')) {
  function notifyIndividualFromAdmin_mysqli(
    mysqli $db_connection,
    int $individualId,
    string $actionKey,
    string $serviceLabel,
    ?int $recordId = null,
    ?string $assignedDateYmd = null
  ): bool {

    if ($individualId <= 0) return false;

    if (session_status() !== PHP_SESSION_ACTIVE) {
      @session_start();
    }

    $createdByType = 'admin';

    $createdById = isset($_SESSION['user_id']) ? (int)$_SESSION['user_id'] : 0;
    if ($createdById <= 0 && isset($_SESSION['admin_id'])) {
      $createdById = (int)$_SESSION['admin_id'];
    }
    if ($createdById <= 0) return false;

    // Pull admin display from DB
    $adminDisplay = htccc_admin_display_name_from_db($db_connection);

    $actionKey = strtolower(trim($actionKey));
    $serviceLabel = trim($serviceLabel) !== '' ? trim($serviceLabel) : 'Service';
    $actionLabel = 'Updated';

    if (
      $actionKey === 'accept' ||
      $actionKey === 'approved' ||
      str_contains($actionKey, 'accept') ||
      str_contains($actionKey, 'approve')
    ) {
      $actionLabel = 'Approved';
    } elseif ($actionKey === 'reupload' || str_contains($actionKey, 'reupload')) {
      $actionLabel = 'Reupload Required';
    } elseif (
      $actionKey === 'reject' ||
      $actionKey === 'rejected' ||
      $actionKey === 'cancel' ||
      $actionKey === 'cancelled' ||
      str_contains($actionKey, 'reject') ||
      str_contains($actionKey, 'cancel')
    ) {
      $actionLabel = 'Rejected';
    }

    if ($assignedDateYmd !== null) {
      $assignedDateYmd = substr(trim($assignedDateYmd), 0, 10);
    }
    if (!$assignedDateYmd || !preg_match('/^\d{4}-\d{2}-\d{2}$/', $assignedDateYmd)) {
      $assignedDateYmd = date('Y-m-d');
    }

    $title = $serviceLabel . ' – ' . $actionLabel;
    $recordIdPart = ($recordId !== null && $recordId > 0) ? (string)$recordId : '-';

    $lines = [];
    $lines[] = 'Admin: ' . $adminDisplay;
    $lines[] = 'Action: ' . $actionLabel;
    $lines[] = 'Service: ' . $serviceLabel;
    $lines[] = 'Record ID: ' . $recordIdPart;
    $lines[] = 'Summary: ' . $serviceLabel . ' | Status: ' . $actionLabel . ' • Date: ' . $assignedDateYmd;

    $body = implode("\n", $lines);

    if (function_exists('mb_strlen')) {
      if (mb_strlen($body) > 2000) $body = mb_substr($body, 0, 2000);
    } else {
      if (strlen($body) > 2000) $body = substr($body, 0, 2000);
    }

    $titleEsc         = mysqli_real_escape_string($db_connection, $title);
    $bodyEsc          = mysqli_real_escape_string($db_connection, $body);
    $createdByTypeEsc = mysqli_real_escape_string($db_connection, $createdByType);

    // created_by_id stores FULL NAME string
    $createdByNameEsc = mysqli_real_escape_string($db_connection, $adminDisplay);

    $sqlNotif = "
      INSERT INTO notifications (title, body, created_by_type, created_by_id)
      VALUES ('{$titleEsc}', '{$bodyEsc}', '{$createdByTypeEsc}', '{$createdByNameEsc}')
    ";

    if (!mysqli_query($db_connection, $sqlNotif)) return false;

    $notificationId = (int)mysqli_insert_id($db_connection);
    if ($notificationId <= 0) return false;

    $sqlRec = "
      INSERT INTO notification_recipients (notification_id, user_type, user_id, status)
      VALUES ({$notificationId}, 'individual', {$individualId}, 'unread')
    ";

    if (!mysqli_query($db_connection, $sqlRec)) return false;

    return true;
  }
