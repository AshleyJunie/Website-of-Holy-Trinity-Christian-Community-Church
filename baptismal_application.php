/**
 * Turn a DB path into a public URL the browser can load.
 */
function htccc_public_url_from_path($path) {
  $path = trim((string)$path);
  if ($path === '') return '';

  $path = str_replace('\\', '/', $path);

  if (preg_match('#^https?://#i', $path)) {
    return $path;
  }

  $scheme  = (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off') ? 'https://' : 'http://';
  $host    = isset($_SERVER['HTTP_HOST']) ? $_SERVER['HTTP_HOST'] : 'localhost';
  $baseUrl = rtrim($scheme . $host, '/');

  $scriptDir    = rtrim(dirname($_SERVER['PHP_SELF']), '/\\'); // "/HTCCC-SYSTEM"
  $rootDirName  = trim($scriptDir, '/');                       // "HTCCC-SYSTEM"

  if (strlen($path) && $path[0] === '/') {
    return $baseUrl . $path;
  }

  if ($rootDirName !== '' && stripos($path, $rootDirName . '/') === 0) {
    return $baseUrl . '/' . ltrim($path, '/');
  }

  return $baseUrl . $scriptDir . '/' . ltrim($path, '/');
}

