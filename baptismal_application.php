/* =========================
   Mail helper
   ========================= */
function htccc_send_mail($toEmail, $toName, $subject, $bodyPlain) {
  if (!filter_var($toEmail, FILTER_VALIDATE_EMAIL)) {
    return ['ok'=>false, 'error'=>'Invalid recipient email', 'error_info'=>''];
  }

  $mail = new PHPMailer(true);
  try {
    $mail->isSMTP();
    $mail->Host       = 'smtp.gmail.com';
    $mail->SMTPAuth   = true;
    $mail->Username   = 'holytrinitychristiancommunityc@gmail.com'; // Gmail address

    // SAFETY: keep your real app password here (you already have it)
    $mail->Password   = 'PUT_YOUR_APP_PASSWORD_HERE';

    $mail->SMTPSecure = PHPMailer::ENCRYPTION_STARTTLS;
    $mail->Port       = 587;
    $mail->CharSet    = 'UTF-8';

    $mail->setFrom('holytrinitychristiancommunityc@gmail.com', 'HTCCC');
    $mail->addAddress($toEmail, $toName ?: $toEmail);

    $mail->isHTML(false);
    $mail->Subject = (string)$subject;
    $mail->Body    = (string)$bodyPlain;
    $mail->AltBody = (string)$bodyPlain;

    $mail->send();
    return ['ok'=>true, 'error'=>'', 'error_info'=>''];
  } catch (Exception $e) {
    return ['ok'=>false, 'error'=>'Mailer Error', 'error_info'=>$mail->ErrorInfo];
  }
}

