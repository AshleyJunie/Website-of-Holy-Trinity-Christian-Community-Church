<!-- ===== JS: Search • Sort • Modal + PDF Download ===== -->
<script>
(function(){
  const $  = (s,root=document)=>root.querySelector(s);
  const $$ = (s,root=document)=>Array.from(root.querySelectorAll(s));

  const tableBody     = $('#tableBody');
  const searchInput   = $('#searchInput');
  const sortBaptizand = $('#sortBaptizand');
  const sortGuardian  = $('#sortGuardian');
  const sortDate      = $('#sortDate');
  const applySort     = $('#applySort');
  const downloadPdf   = $('#downloadPdf');

  // Modal elements
  const backdrop  = $('#viewModalBackdrop');
  const modalBody = $('#modalBody');
  const closeBtn  = $('#modalCloseBtn');

  // Image viewer elements
  const imgBox         = document.getElementById('imgBox');
  const imgBoxImg      = document.getElementById('imgBoxImg');
  const imgBoxCloseBtn = document.getElementById('imgBoxCloseBtn');

  const esc = s => (s ?? '').toString().replace(/[&<>"']/g, c=>({ '&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;' }[c]));
  function textOrDash(v){
    const val = (v || '').toString().trim();
    return val ? esc(val) : '<span class="muted">—</span>';
  }

  function hasGuardianData(tr){
    const g = [
      tr.dataset.guardian_lastname,
      tr.dataset.guardian_firstname,
      tr.dataset.guardian_middlename,
      tr.dataset.guardian_ext,
      tr.dataset.guardian_contactnum
    ].map(x => (x || '').trim());
    return g.some(v => v.length > 0);
  }

  function openModal(tr){
    const serviceDateDisplay = tr.dataset.service_date
      ? new Date(tr.dataset.service_date).toLocaleDateString(undefined,{year:'numeric',month:'short',day:'2-digit'})
      : '';

    const hasGuardian = hasGuardianData(tr);

    let html = `
      <section class="modal-section">
        <h4 class="modal-section-title">Service Information</h4>
        <div class="kv">
          <div class="kv-label">Service Date</div>
          <div class="kv-value">${textOrDash(serviceDateDisplay)}</div>
        </div>
      </section>

      <section class="modal-section">
        <h4 class="modal-section-title">Baptizand Information</h4>
        <div class="kv">
          <div class="kv-label">Last Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.baptized_lastname)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">First Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.baptized_firstname)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Middle Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.baptized_middlename)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Extension</div>
          <div class="kv-value">${textOrDash(tr.dataset.baptized_ext)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Birthdate</div>
          <div class="kv-value">${textOrDash(tr.dataset.baptized_birthdate)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Email Address</div>
          <div class="kv-value">${textOrDash(tr.dataset.email_address)}</div>
        </div>
      </section>
    `;

    if (hasGuardian){
      html += `
        <section class="modal-section">
          <h4 class="modal-section-title">Guardian Information</h4>
          <div class="kv">
            <div class="kv-label">Last Name</div>
            <div class="kv-value">${textOrDash(tr.dataset.guardian_lastname)}</div>
          </div>
          <div class="kv">
            <div class="kv-label">First Name</div>
            <div class="kv-value">${textOrDash(tr.dataset.guardian_firstname)}</div>
          </div>
          <div class="kv">
            <div class="kv-label">Middle Name</div>
            <div class="kv-value">${textOrDash(tr.dataset.guardian_middlename)}</div>
          </div>
          <div class="kv">
            <div class="kv-label">Extension</div>
            <div class="kv-value">${textOrDash(tr.dataset.guardian_ext)}</div>
          </div>
          <div class="kv">
            <div class="kv-label">Contact Number</div>
            <div class="kv-value">${textOrDash(tr.dataset.guardian_contactnum)}</div>
          </div>
        </section>
      `;
    }

    modalBody.innerHTML = html;
    backdrop.classList.add('open');
    backdrop.setAttribute('aria-hidden','false');
  }

  function closeModal(){
    backdrop.classList.remove('open');
    backdrop.setAttribute('aria-hidden','true');
  }

  backdrop.addEventListener('click', e=>{ if(e.target === backdrop) closeModal(); });
  closeBtn.addEventListener('click', closeModal);
  document.addEventListener('keydown', e=>{ if(e.key === 'Escape') closeModal(); });

  function wireViewButtons(){
    $$('.btn-view', tableBody).forEach(btn=>{
      btn.onclick = () => {
        const tr = btn.closest('tr');
        if (!tr) return;
        openModal(tr);
      };
    });
  }
  wireViewButtons();

  function applyFilters(){
    const q = (searchInput.value || '').toLowerCase().trim();
    const rows = $$('#tableBody tr');

    rows.forEach(r=>{
      if (r.classList.contains('empty-row')) return;
      r.style.display = '';
      if (q){
        const hay = [
          r.dataset.service_date,
          r.dataset.baptized_lastname,
          r.dataset.baptized_firstname,
          r.dataset.baptized_middlename,
          r.dataset.baptized_ext,
          r.dataset.baptized_birthdate,
          r.dataset.email_address,
          r.dataset.guardian_lastname,
          r.dataset.guardian_firstname,
          r.dataset.guardian_middlename,
          r.dataset.guardian_ext,
          r.dataset.guardian_contactnum
        ].join(' ').toLowerCase();
        if (!hay.includes(q)) r.style.display = 'none';
      }
    });

    const dateVal  = sortDate.value;
    const baptVal  = sortBaptizand.value;
    const guardVal = sortGuardian.value;

    const visible = rows.filter(r=>!r.classList.contains('empty-row') && r.style.display !== 'none');

    visible.sort((a,b)=>{
      if (dateVal === 'date_desc') {
        const da = Date.parse(a.dataset.service_date || '1970-01-01');
        const db = Date.parse(b.dataset.service_date || '1970-01-01');
        if (db !== da) return db - da;
      } else if (baptVal === 'baptizand') {
        const la = (a.dataset.baptized_lastname || '').toLowerCase();
        const lb = (b.dataset.baptized_lastname || '').toLowerCase();
        if (la !== lb) return la < lb ? -1 : 1;
      } else if (guardVal === 'guardian') {
        const la = (a.dataset.guardian_lastname || '').toLowerCase();
        const lb = (b.dataset.guardian_lastname || '').toLowerCase();
        if (la !== lb) return la < lb ? -1 : 1;
      }

      const da2 = Date.parse(a.dataset.service_date || '1970-01-01');
      const db2 = Date.parse(b.dataset.service_date || '1970-01-01');
      if (db2 !== da2) return db2 - da2;

      const la2 = (a.dataset.baptized_lastname || '').toLowerCase();
      const lb2 = (b.dataset.baptized_lastname || '').toLowerCase();
      if (la2 !== lb2) return la2 < lb2 ? -1 : 1;
      return 0;
    });

    visible.forEach(r=>tableBody.appendChild(r));
    rows.filter(r=>r.style.display === 'none').forEach(r=>tableBody.appendChild(r));

    wireViewButtons();
  }

  searchInput.addEventListener('input', applyFilters);
  applySort.addEventListener('click', applyFilters);

  // ===== PDF DOWNLOAD =====
  if (downloadPdf) {
    downloadPdf.addEventListener('click', () => {
      window.location = 'done-service-baptism.php?download=baptism_pdf';
    });
  }

  // ===== IMAGE VIEWER (future-proof) =====
  function openImgBox(src){
    if (!imgBox || !imgBoxImg) return;
    imgBoxImg.src = src;
    imgBox.setAttribute('aria-hidden','false');
  }
  function closeImgBox(){
    if (!imgBox || !imgBoxImg) return;
    imgBoxImg.src = '';
    imgBox.setAttribute('aria-hidden','true');
  }
  document.addEventListener('click', (e) => {
    const btn = e.target.closest('.view-image[data-img]');
    if (!btn) return;
    e.preventDefault();
    const src = btn.getAttribute('data-img');
    if (src) openImgBox(src);
  });
  if (imgBox) {
    imgBox.addEventListener('click', (e) => {
      if (e.target === imgBox) closeImgBox();
    });
  }
  if (imgBoxCloseBtn) {
    imgBoxCloseBtn.addEventListener('click', closeImgBox);
  }

  // Initial
  applyFilters();
})();
</script>

