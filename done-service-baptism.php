/* =============================================================================
   ADMIN DISPLAY NAME (used in PDF header)
   NOTE: Update the table/columns below to match your admin table.
   - If your session key is different, update $adminId line too.
   ========================================================================== */
function getAdminDisplayName(mysqli $db): string {
  $adminId = $_SESSION['admin_id'] ?? $_SESSION['user_id'] ?? null;
  if (!$adminId) return 'Admin';

  // ✅ CHANGE THIS to your real admin table + columns
  $sql = "SELECT admin_lastname, admin_firstname, admin_middlename
          FROM admin_table
          WHERE admin_id = ?
          LIMIT 1";

  if ($stmt = $db->prepare($sql)) {
    $stmt->bind_param("i", $adminId);
    $stmt->execute();
    $res = $stmt->get_result();
    if ($row = $res->fetch_assoc()) {
      $ln = trim($row['admin_lastname'] ?? '');
      $fn = trim($row['admin_firstname'] ?? '');
      $mn = trim($row['admin_middlename'] ?? '');
      $name = trim(($ln ? $ln . ',' : '') . ' ' . $fn . ' ' . $mn);
      return $name ?: 'Admin';
    }
  }
  return 'Admin';
}

