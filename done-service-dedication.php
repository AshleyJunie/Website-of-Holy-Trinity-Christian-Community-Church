<!-- ===== JS: Search • Sort • Modal (pattern copied from wedding-list.php) ===== -->
<script>
(function(){
  const $  = (s,root=document)=>root.querySelector(s);
  const $$ = (s,root=document)=>Array.from(root.querySelectorAll(s));
  const tableBody   = $('#tableBody');
  const searchInput = $('#searchInput');
  const sortGuardian = $('#sortGuardian');
  const sortChild    = $('#sortChild');
  const sortDate     = $('#sortDate');
  const applySort    = $('#applySort');

  // Modal elements
  const backdrop = $('#viewModalBackdrop');
  const modalBody = $('#modalBody');
  const closeBtn = $('#modalCloseBtn');

  const esc = s => (s ?? '').toString().replace(/[&<>"']/g, c=>({ '&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;' }[c]));

  function textOrDash(v){
    const val = (v || '').toString().trim();
    return val ? esc(val) : '<span class="muted">—</span>';
  }

  function openModal(tr){
    const serviceDateStr = tr.dataset.service_date
      ? new Date(tr.dataset.service_date).toLocaleDateString(undefined,{year:'numeric',month:'short',day:'2-digit'})
      : '';

    const html = `
      <section class="modal-section">
        <h4 class="modal-section-title">Service Information</h4>
        <div class="kv">
          <div class="kv-label">Service Date</div>
          <div class="kv-value">${textOrDash(serviceDateStr)}</div>
        </div>
      </section>

      <section class="modal-section">
        <h4 class="modal-section-title">Guardian Information</h4>
        <div class="kv">
          <div class="kv-label">Last Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.guardian_lastname)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">First Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.guardian_firstname)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Middle Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.guardian_middlename)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Extension</div>
          <div class="kv-value">${textOrDash(tr.dataset.guardian_ext)}</div>
        </div>
      </section>

      <section class="modal-section">
        <h4 class="modal-section-title">Child Information</h4>
        <div class="kv">
          <div class="kv-label">Last Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.child_lastname)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">First Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.child_firstname)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Middle Name</div>
          <div class="kv-value">${textOrDash(tr.dataset.child_middlename)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Extension</div>
          <div class="kv-value">${textOrDash(tr.dataset.child_ext)}</div>
        </div>
        <div class="kv">
          <div class="kv-label">Birthdate</div>
          <div class="kv-value">${textOrDash(tr.dataset.child_birthdate)}</div>
        </div>
      </section>
    `;

    modalBody.innerHTML = html;

    backdrop.classList.add('open');
    backdrop.setAttribute('aria-hidden','false');
  }

  function closeModal(){
    backdrop.classList.remove('open');
    backdrop.setAttribute('aria-hidden','true');
  }

  backdrop.addEventListener('click', e=>{ if(e.target === backdrop) closeModal(); });
  closeBtn.addEventListener('click', closeModal);
  document.addEventListener('keydown', e=>{ if(e.key === 'Escape') closeModal(); });

  function wireViewButtons(){
    $$('.btn-view', tableBody).forEach(btn=>{
      btn.onclick = () => {
        const tr = btn.closest('tr');
        openModal(tr);
      };
    });
  }
  wireViewButtons();

  function applyFilters(){
    const q = (searchInput.value || '').toLowerCase().trim();
    const rows = $$('#tableBody tr');

    rows.forEach(r=>{
      if (r.classList.contains('empty-row')) return;
      r.style.display = '';
      if(q){
        const hay = [
          r.dataset.service_date,
          r.dataset.guardian_lastname,
          r.dataset.guardian_firstname,
          r.dataset.guardian_middlename,
          r.dataset.guardian_ext,
          r.dataset.child_lastname,
          r.dataset.child_firstname,
          r.dataset.child_middlename,
          r.dataset.child_ext,
          r.dataset.child_birthdate
        ].join(' ').toLowerCase();
        if(!hay.includes(q)) r.style.display = 'none';
      }
    });

    // Sort
    const dateVal     = sortDate.value;
    const guardianVal = sortGuardian.value;
    const childVal    = sortChild.value;

    const visible = rows.filter(r=>!r.classList.contains('empty-row') && r.style.display!=='none');

    visible.sort((a,b)=>{
      if (dateVal === 'date_desc') {
        const da = Date.parse(a.dataset.service_date || '1970-01-01');
        const db = Date.parse(b.dataset.service_date || '1970-01-01');
        if (db !== da) return db - da;
      } else if (guardianVal === 'guardian') {
        const la = (a.dataset.guardian_lastname || '').toLowerCase();
        const lb = (b.dataset.guardian_lastname || '').toLowerCase();
        if (la !== lb) return la < lb ? -1 : 1;
      } else if (childVal === 'child') {
        const la = (a.dataset.child_lastname || '').toLowerCase();
        const lb = (b.dataset.child_lastname || '').toLowerCase();
        if (la !== lb) return la < lb ? -1 : 1;
      }

      // Tie breakers (service date desc, guardian last name)
      const da2 = Date.parse(a.dataset.service_date || '1970-01-01');
      const db2 = Date.parse(b.dataset.service_date || '1970-01-01');
      if (db2 !== da2) return db2 - da2;
      const la2 = (a.dataset.guardian_lastname || '').toLowerCase();
      const lb2 = (b.dataset.guardian_lastname || '').toLowerCase();
      if (la2 !== lb2) return la2 < lb2 ? -1 : 1;
      return 0;
    });

    visible.forEach(r=>tableBody.appendChild(r));
    rows.filter(r=>r.style.display==='none').forEach(r=>tableBody.appendChild(r));

    wireViewButtons();
  }

  searchInput.addEventListener('input', applyFilters);
  applySort.addEventListener('click', applyFilters);

  // Initial state
  applyFilters();
})();
</script>

