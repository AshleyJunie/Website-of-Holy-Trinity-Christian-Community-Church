/* =============================================================================
   Admin display name (DB)
   - Pulls from admin table using session admin_id
   ============================================================================= */
function htccc_admin_display_name_from_db(mysqli $db_connection): string {
  if (session_status() !== PHP_SESSION_ACTIVE) {
    @session_start();
  }

  // CHANGE THIS if your admin table name is different
  $adminTable = 'admin_table';

  // Admin id from session (adjust if your system uses different key)
  $adminId = 0;
  if (!empty($_SESSION['admin_id'])) {
    $adminId = (int)$_SESSION['admin_id'];
  } elseif (!empty($_SESSION['user_id'])) {
    $adminId = (int)$_SESSION['user_id'];
  }

  // If cannot resolve ID, fallback to any session name if exists
  if ($adminId <= 0) {
    if (!empty($_SESSION['admin_lastname']) || !empty($_SESSION['admin_firstname'])) {
      $ln = trim((string)($_SESSION['admin_lastname'] ?? ''));
      $fn = trim((string)($_SESSION['admin_firstname'] ?? ''));
      $mn = trim((string)($_SESSION['admin_middlename'] ?? ''));
      $out = trim($ln . ', ' . $fn . ' ' . $mn);
      return trim(preg_replace('/\s+/', ' ', $out)) ?: 'Admin';
    }
    return 'Admin';
  }

  // Title-case helper (optional)
  $titleCase = function ($s) {
    $s = trim((string)$s);
    if ($s === '') return '';
    return preg_replace_callback(
      '/\b(\p{L})(\p{L}*)/u',
      fn($m) => mb_strtoupper($m[1]).mb_strtolower($m[2]),
      $s
    );
  };

  // Query admin table
  $sql = "SELECT admin_lastname, admin_firstname, admin_middlename
          FROM {$adminTable}
          WHERE admin_id = ?
          LIMIT 1";

  if ($stmt = mysqli_prepare($db_connection, $sql)) {
    mysqli_stmt_bind_param($stmt, "i", $adminId);
    mysqli_stmt_execute($stmt);
    $res = mysqli_stmt_get_result($stmt);

    if ($row = mysqli_fetch_assoc($res)) {
      $ln = $titleCase($row['admin_lastname'] ?? '');
      $fn = $titleCase($row['admin_firstname'] ?? '');
      $mn = $titleCase($row['admin_middlename'] ?? '');

      mysqli_stmt_close($stmt);

      // Format: Lastname, Firstname Middlename
      $name = '';
      if ($ln !== '' && $fn !== '') {
        $name = $ln . ', ' . $fn;
      } elseif ($ln !== '') {
        $name = $ln;
      } elseif ($fn !== '') {
        $name = $fn;
      }

      if ($mn !== '') {
        $name .= ($name !== '' ? ' ' : '') . $mn;
      }

      return $name !== '' ? $name : ('Admin #' . $adminId);
    }

    mysqli_stmt_close($stmt);
  }

  return 'Admin #' . $adminId;
}

